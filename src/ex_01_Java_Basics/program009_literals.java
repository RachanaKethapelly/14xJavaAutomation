package ex_01_Java_Basics;

public class program009_literals
{
    public static void main(String[] args) {
        //literals Literals are the actual values assigned, Literals can be Numeric and Non Numeric.
         int a = 10;
        System.out.println(a); // integral literals
        final float pi = 3.14f;
        System.out.println(pi); // floating literals
        boolean b = true;
        System.out.println(b); // boolean literals
        char c = 'r', d = 'a' , e = 'c' , f = 'h' , g = 'u';
        System.out.println("" + c + d + e + f + g); //character literals
        String s = "rachana";
        System.out.println(s); // string literals

    }
}
